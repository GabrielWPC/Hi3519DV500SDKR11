const drv_pke_ecc_curve *get_ecc_curve(drv_pke_ecc_curve_type curve_type)
{
    if (curve_type >= DRV_PKE_ECC_TYPE_MAX) {
        return TD_NULL;
    }

    if (crypto_ecc_support(curve_type) == TD_FALSE) {
        return TD_NULL;
    }
    if (curve_type == DRV_PKE_ECC_TYPE_SM2 && !crypto_sm_support(CRYPTO_SM_ALG_SM2)) {
        return TD_NULL;
    }
    return g_ecc_curve_list[curve_type];
}
